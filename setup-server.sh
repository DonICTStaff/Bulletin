echo -e "  ${DIM}в”Ӯ${NC}   Hostname: ${GREEN}${SERVER_HOSTNAME}${NC}"
echo -e "  ${DIM}в”Ӯ${NC}   Port:     ${GREEN}${SERVER_PORT}${NC}"
echo -e "  ${DIM}в”Ӯ${NC}   Install:  ${GREEN}${INSTALL_DIR}${NC}"
