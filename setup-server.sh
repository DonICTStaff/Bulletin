if [[ ! "$CONFIRM" =~ ^[Yy]$ ]]; then
    echo ""
    info "Installation cancelled."
    echo ""
    exit 0
fi

# в”Җв”Җ Require root в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
if [[ $EUID -ne 0 ]]; then
    error "This installer must be run as root (use sudo)."
fi

TOTAL_STEPS=7

# в”Җв”Җ Step 1: System packages в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
step 1 $TOTAL_STEPS "Installing system packages"
spinner_start "Updating package lists..."
apt-get update -qq > /dev/null 2>&1
spinner_stop "Package lists updated"

spinner_start "Installing dependencies (python3, nginx, git, curl)..."
apt-get install -y -qq \
    python3 \
    python3-venv \
    python3-pip \
    nginx \
    git \
    curl \
    > /dev/null 2>&1
spinner_stop "System packages installed"

# в”Җв”Җ Step 2: Clone repository в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
step 2 $TOTAL_STEPS "Fetching source code"
if [[ -d "$INSTALL_DIR" ]]; then
    spinner_start "Updating existing installation..."
    cd "$INSTALL_DIR"
    git pull origin "$BRANCH" > /dev/null 2>&1 || true
    spinner_stop "Repository updated"
else
    spinner_start "Cloning repository..."
    git clone --branch "$BRANCH" "https://github.com/${REPO_OWNER}/${REPO_NAME}.git" "$INSTALL_DIR" > /dev/null 2>&1
    spinner_stop "Repository cloned to ${INSTALL_DIR}"
fi

# в”Җв”Җ Step 3: Python venv в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
step 3 $TOTAL_STEPS "Setting up Python environment"
spinner_start "Creating virtual environment..."
python3 -m venv "$VENV_DIR" > /dev/null 2>&1
spinner_stop "Virtual environment created"

spinner_start "Installing Python dependencies..."
source "${VENV_DIR}/bin/activate"
pip install -qq -r "${INSTALL_DIR}/requirements.txt" > /dev/null 2>&1
deactivate
spinner_stop "Python dependencies installed"

# в”Җв”Җ Step 4: Database в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
step 4 $TOTAL_STEPS "Initializing database"
spinner_start "Creating database and admin user..."
SECRET_KEY=$(python3 -c 'import secrets; print(secrets.token_hex(32))')
export BULLETIN_SECRET_KEY="${SECRET_KEY}"
source "${VENV_DIR}/bin/activate"
python3 -c "
from app import app, init_db, db, User
with app.app_context():
    init_db()
    from werkzeug.security import generate_password_hash
    admin = User.query.filter_by(username='admin').first()
    if admin:
        admin.password_hash = generate_password_hash('${ADMIN_PASS}')
        db.session.commit()
" > /dev/null 2>&1
deactivate
spinner_stop "Database initialized"

# в”Җв”Җ Step 5: Config в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
step 5 $TOTAL_STEPS "Writing configuration"
cat > "${INSTALL_DIR}/config.env" << EOF
BULLETIN_SECRET_KEY=${SECRET_KEY}
EOF
chmod 600 "${INSTALL_DIR}/config.env"
mkdir -p "${INSTALL_DIR}/uploads"
success "Config saved to ${INSTALL_DIR}/config.env"

# в”Җв”Җ Step 6: Systemd service в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
step 6 $TOTAL_STEPS "Creating systemd service"
cat > "/etc/systemd/system/${SERVICE_NAME}.service" << SVCEOF
[Unit]
Description=Bulletin Board Flask Server
After=network-online.target
Wants=network-online.target

[Service]
Type=simple
User=www-data
Group=www-data
WorkingDirectory=${INSTALL_DIR}
EnvironmentFile=${INSTALL_DIR}/config.env
ExecStart=${VENV_DIR}/bin/python ${INSTALL_DIR}/app.py
Restart=always
RestartSec=5
StandardOutput=journal
StandardError=journal

[Install]
WantedBy=multi-user.target
SVCEOF

chown -R www-data:www-data "${INSTALL_DIR}/uploads"
